package Start;

public class SwitchStatements {
    public static void main(String []args){
        int myAge=22;
        switch (myAge){
            case 1:
                System.out.println("My age is "+myAge);
                break;
            case 22:
                System.out.println("My age is "+myAge);
                break;
            case 3:
                System.out.println("My age is "+myAge);
                break;
                default:
                    System.out.println("It seems your age is not defined!!!");
        }
    }
}
